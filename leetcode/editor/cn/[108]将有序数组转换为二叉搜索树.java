import java.util.Arrays;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public TreeNode sortedArrayToBST(int[] nums) {
        if (nums == null || nums.length == 0) {
            return null;
        }
        TreeNode treeNode = new TreeNode();
        // 因为数组nums已经有序了，所以直接取中间的节点，这样左边的就是小于中间节点的值，右边是大于的值，保证了当前树的搜索度
        treeNode.val = nums[nums.length / 2];
        // 每次都使用递归，利用分治思想，让每个左右子树都是一颗平衡二叉搜索树
        treeNode.left = sortedArrayToBST(Arrays.copyOfRange(nums, 0, nums.length / 2));
        treeNode.right = sortedArrayToBST(Arrays.copyOfRange(nums, nums.length / 2 + 1, nums.length));
        return treeNode;
    }
}

